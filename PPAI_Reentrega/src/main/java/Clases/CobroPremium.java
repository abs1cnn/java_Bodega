package Clases;


public class CobroPremium {

	private Object nroOperacionMercadoPago;
	private Object esAnual;
	private Object fechaPago;
	private Object monto;


	public Object getEsAnual() {
		return esAnual;
	}

	public void setEsAnual(Object esAnual) {
		this.esAnual = esAnual;
	}


	public Object getFechaPago() {
		return fechaPago;
	}


	public void setFechaPago(Object fechaPago) {
		this.fechaPago = fechaPago;
	}


	public Object getMonto() {
		return monto;
	}

	public void setMonto(Object monto) {
		this.monto = monto;
	}

	public Object getNroOperacionMercadoPago() {
		return nroOperacionMercadoPago;
	}

	public void setNroOperacionMercadoPago(Object nroOperacionMercadoPago) {
		this.nroOperacionMercadoPago = nroOperacionMercadoPago;
	}
}